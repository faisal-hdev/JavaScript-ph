/**
 * for (        ;       ;       ){
 * }
 * 
 * for (first part; second part; third part) {
    //loop statement
}
 * for (loop ar declaration; loop ar condition)
 */

// while loop
let num = 0; // loop ar variable
while (num < 5) {
  // loop ar condition
  console.log(num); // loop ar statement
  num++; // loop ar variable change
}

// for loop
for (let num = 0; num < 5; num++) {
  console.log(num);
}
